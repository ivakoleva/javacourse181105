public class RetrievingDataException1 extends Exception {
    public RetrievingDataException1() {
        super();
    }

    public RetrievingDataException1(String message) {
        super(message);
    }

    public RetrievingDataException1(String message, Throwable cause) {
        super(message, cause);
    }

    public RetrievingDataException1(Throwable cause) {
        super(cause);
    }

    protected RetrievingDataException1(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
